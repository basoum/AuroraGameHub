    /**
     * Saves image file and scales it to fit game covers.
     * <p>
     * @param file
     *             <p>
     * @return
     */
    private int SCALE_WIDTH_PARAM_BLANK = 398;

    private int SCALE_WIDTH_PARAM = 330;

    private int SCALE_HEIGHT_PARAM_BLANK = 498;

    private int SCALE_HEIGHT_PARAM = 466;

    public AImagePane processNewCoverArtImage(File file) {

        // Check if image is already available
        AFileManager fileIO = dashboardUI.getStartUI().getFileIO();
        Boolean loadedImage = true;
        String fileName = file.getName();
        AImagePane currentImagePane = new AImagePane();

        // Check if image has already been proccessed in Game Data folder
        try {
            ImageIcon img = fileIO.findImg("Game Data",
                                           fileName);
            currentImagePane.setImage(img, img.getIconHeight(),
                                      img.getIconWidth());

        } catch (Exception ex) {
            loadedImage = false;
        }

        if (!loadedImage) {
            // Resize and Save image
            BufferedImage img;
            try {
                img = ImageIO.read(file);
                BufferedImage scaledImg = AImage.resizeBufferedImage(img,
                                                                     SCALE_WIDTH_PARAM,
                                                                     SCALE_HEIGHT_PARAM);

                AImage blankCover = new AImage("Blank-Case.png", SCALE_WIDTH_PARAM_BLANK, SCALE_HEIGHT_PARAM_BLANK);
                BufferedImage overlay = ImageIO.read(new File(blankCover.getImagePath()));



                int width = 67;
                int height = 20;
                BufferedImage newImage = new BufferedImage(
                        512,
                        512,
                        BufferedImage.TYPE_INT_ARGB);

                Graphics g = newImage.getGraphics();

                g.drawImage(scaledImg, width + (SCALE_WIDTH_PARAM_BLANK - SCALE_WIDTH_PARAM) - 18, height - 2, null);
                g.drawImage(overlay, 0, 0, null);
                g.dispose();

                currentImagePane.setImage(new ImageIcon(newImage),
                                          512,
                                          512);

                fileIO.writeImage(currentImagePane, fileName, "Game Data");



            } catch (IOException ex) {
                java.util.logging.Logger.getLogger(LibraryLogic.class
                        .getName())
                        .log(Level.SEVERE, null, ex);
            }
        }

        if (currentImagePane != null) {
            currentImagePane.setPreferredSize(new Dimension(currentImagePane
                    .getRealImageHeight(), currentImagePane
                                                            .getRealImageWidth()));
            currentImagePane.setImageFileName(fileName);
        }

        return currentImagePane;

    }

    public void flashInvalidStatus(final AImage status) {

        AThreadWorker worker = new AThreadWorker(new ActionListener() {
            private int count;

            private boolean flashIdleIcon;

            @Override
            public void actionPerformed(ActionEvent e) {

                if (status.getImgURl().equals("addUI_badge_idle.png") || status
                        .getImgURl().equals("addUI_badge_idle_glow.png")) {
                    flashIdleIcon = true;
                }

                while (count < 6) {

                    if (flashIdleIcon) {
                        if (count % 2 == 0) {
                            status.setImgURl("addUI_badge_idle_glow.png");
                        } else {
                            status.setImgURl("addUI_badge_idle.png");
                        }
                    } else {
                        if (count % 2 == 0) {
                            status.setImgURl("addUI_badge_invalid_noglow.png");
                        } else {
                            status.setImgURl("addUI_badge_invalid.png");
                        }
                    }
                    count++;
                    try {
                        Thread.sleep(200);


                    } catch (InterruptedException ex) {
                        java.util.logging.Logger.getLogger(LibraryLogic.class
                                .getName())
                                .log(Level.SEVERE, null, ex);
                    }
                }


            }
        });

        worker.startOnce();

    }

